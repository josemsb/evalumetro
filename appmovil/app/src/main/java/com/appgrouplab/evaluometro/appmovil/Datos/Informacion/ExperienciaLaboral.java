package com.appgrouplab.evaluometro.appmovil.Datos.Informacion;

public class ExperienciaLaboral {
    private String dni;
    private String centrotrabajo;
    private String ocupacionprofesional;
    private String aniotrabajodesde;
    private String aniotrabajohasta;
    private String trabajopais;

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getCentrotrabajo() {
        return centrotrabajo;
    }

    public void setCentrotrabajo(String centrotrabajo) {
        this.centrotrabajo = centrotrabajo;
    }

    public String getOcupacionprofesional() {
        return ocupacionprofesional;
    }

    public void setOcupacionprofesional(String ocupacionprofesional) {
        this.ocupacionprofesional = ocupacionprofesional;
    }

    public String getAniotrabajodesde() {
        return aniotrabajodesde;
    }

    public void setAniotrabajodesde(String aniotrabajodesde) {
        this.aniotrabajodesde = aniotrabajodesde;
    }

    public String getAniotrabajohasta() {
        return aniotrabajohasta;
    }

    public void setAniotrabajohasta(String aniotrabajohasta) {
        this.aniotrabajohasta = aniotrabajohasta;
    }

    public String getTrabajopais() {
        return trabajopais;
    }

    public void setTrabajopais(String trabajopais) {
        this.trabajopais = trabajopais;
    }

    public String getTrabajodepartamento() {
        return trabajodepartamento;
    }

    public void setTrabajodepartamento(String trabajodepartamento) {
        this.trabajodepartamento = trabajodepartamento;
    }

    private String trabajodepartamento;
}
